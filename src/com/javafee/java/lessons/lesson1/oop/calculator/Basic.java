package com.javafee.java.lessons.lesson1.oop.calculator;

public class Basic {
	public double addition(double first, double second) {
		return first + second;
	}
	public double subtraction(double first, double second) {
		return first - second;
	}
	public double todivide(double first, double second) {
		return first - second;
	}
	public double multiply(double first, double second) {
		return first * second;
	}
}
